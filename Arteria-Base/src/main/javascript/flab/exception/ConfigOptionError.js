/**
 * Error to be thrown when a static configuration is missing or has
 * an invalid value. This should not be called for whatever you also
 * call configuration if its source is separately loaded and not
 * packed into the configuration.
 * 
 * @class flab.exception.ConfigOptionError
 */
flab.exception.ConfigOptionError = flab.exception.builder(
    'ConfigOptionError',
    flab.exception.ConstraintViolation
);
